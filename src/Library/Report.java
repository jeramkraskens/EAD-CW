       private void loadReturnDateData() {
    DefaultTableModel model = (DefaultTableModel) tableReturn.getModel();
    model.setRowCount(0); // clear existing data

    try {
        Connection con = DatabaseConnection.getConnection();
        String sql = "SELECT * FROM returnBook";
        Statement stmt = con.createStatement();
        ResultSet rs = stmt.executeQuery(sql);

        while (rs.next()) {
            String id = rs.getString("id");
            String BookName = rs.getString("BookName");
            String BDate = rs.getString("BDate");
            String Breturn = rs.getString("Breturn");
            
            
            model.addRow(new Object[]{id,BookName,BDate,Breturn});
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error Return  Data: " + e.getMessage());
    }
    
}
    
    
    
