   private void loadMemberData() {
    DefaultTableModel model = (DefaultTableModel) tableMember.getModel();
    model.setRowCount(0); // clear existing data

    try {
        Connection con = DatabaseConnection.getConnection();
        String sql = "SELECT * FROM member2";
        Statement stmt = con.createStatement();
        ResultSet rs = stmt.executeQuery(sql);

        while (rs.next()) {
            String id = rs.getString("id");
            String name = rs.getString("name");
            String phone = rs.getString("phone");
            
            model.addRow(new Object[]{id,name,phone});
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error Member Data: " + e.getMessage());
    }
    
}
    
    
    
    
    
    public boolean validateForm(){
    
        boolean status =  false;
        
      if(txtMemberId.getText().equals("")||txtMemberName.getText().equals("")||txtPhoneNo.getText().equals("")){
      
      status =  true;
     
      }
      
      return status;
}
    
  
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
    
            if (validateForm()) {

            JOptionPane.showMessageDialog(rootPane, "Please Fill All Fields");

        } else {

           Connection conn = DatabaseConnection.getConnection();

            try {

                String sql = "INSERT INTO member2 VALUES (?,?,?)";

                stmt = conn.prepareStatement(sql);

                stmt.setString(1, txtMemberId.getText().trim());
                stmt.setString(2, txtMemberName.getText().trim());
                stmt.setString(3, txtPhoneNo.getText().trim());
             

                int result = stmt.executeUpdate();
                if (result > 0) {

                    JOptionPane.showMessageDialog(rootPane, "" + "Member Register Successfull");

                } else {

                    JOptionPane.showMessageDialog(rootPane, "" + "Ragister Fail");

                }
            } catch (Exception ee) {

                JOptionPane.showMessageDialog(rootPane, "" + "Try Again");
            }
      loadMemberData();
        }    // TODO add your handling code here:
    }//GEN-LAST:event_btnSaveActionPerformed

