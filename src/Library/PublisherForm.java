    private void loadPublisherData() {
    DefaultTableModel model = (DefaultTableModel) tablePublisher.getModel();
    model.setRowCount(0); // clear existing data

    try {
        Connection con = DatabaseConnection.getConnection();
        String sql = "SELECT * FROM publisher";
        Statement stmt = con.createStatement();
        ResultSet rs = stmt.executeQuery(sql);

        while (rs.next()) {
            String Id = rs.getString("Id");
            String Name = rs.getString("Name");
            String Address = rs.getString("Address");
            String PhoneNo = rs.getString("PhoneNo");
            
            model.addRow(new Object[]{Id, Name, Address, PhoneNo});
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading publisher data: " + e.getMessage());
    }
}
