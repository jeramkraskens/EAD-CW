    private void btnReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReturnActionPerformed

        try (Connection conn = DatabaseConnection.getConnection()) {
            conn.setAutoCommit(false); // Start transaction

            // 1. Insert into returnBook
            String insertSQL = "INSERT INTO returnBook (id, BookName, BDate, Breturn) VALUES (?, ?, ?, ?)";
            try (PreparedStatement insertStmt = conn.prepareStatement(insertSQL)) {

                SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
                String date = df.format(txtDate.getDate());
                String rDate = df.format(txtRdate.getDate());

                insertStmt.setString(1, cmbId.getSelectedItem().toString());  // Use consistent combo box
                insertStmt.setString(2, txtName.getText().trim());
                insertStmt.setString(3, date);
                insertStmt.setString(4, rDate);

                int insertResult = insertStmt.executeUpdate();

                if (insertResult > 0) {
                    // 2. Delete from IssueBook
                    String deleteSQL = "DELETE FROM IssueBook WHERE id = ?";
                    try (PreparedStatement deleteStmt = conn.prepareStatement(deleteSQL)) {
                        deleteStmt.setString(1, cmbId.getSelectedItem().toString());

                        int deleteResult = deleteStmt.executeUpdate();
                        if (deleteResult > 0) {
                            conn.commit(); // Commit both operations
                            JOptionPane.showMessageDialog(rootPane, "Book Returned and Record Deleted Successfully");
                        } else {
                            conn.rollback();
                            JOptionPane.showMessageDialog(rootPane, "Failed to Delete Issue Record");
                        }
                    }
                } else {
                    conn.rollback();
                    JOptionPane.showMessageDialog(rootPane, "Failed to Insert Return Record");
                }
            } catch (Exception e) {
                conn.rollback();
                JOptionPane.showMessageDialog(rootPane, "Transaction Failed: " + e.getMessage());
            } finally {
                conn.setAutoCommit(true); // Restore auto-commit mode
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Connection Error: " + e.getMessage());
        }

    }//GEN-LAST:event_btnReturnActionPerformed

